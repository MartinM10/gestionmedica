DROP TABLE USUARIOS 	IF EXISTS;
DROP TABLE SECUENCIAS	IF EXISTS;
DROP TABLE LECTURAS	 	IF EXISTS;

CREATE TABLE USUARIOS(
	DNI					VARCHAR(100)	NOT NULL,
	NOMBRE				VARCHAR(100)	,
	APELLIDO1			VARCHAR(100)	,
	APELLIDO2			VARCHAR(100)	,
	SEXO				VARCHAR(100)	,
	FECHA_NACIMIENTO	DATE			,
	ALTURA				INT				,
	OBSERVACIONES		VARCHAR(100)	,
	
	PRIMARY KEY (DNI)
);

CREATE TABLE LECTURAS(
	ID					INT				NOT NULL,
	DNI					VARCHAR(100)	NOT NULL,
	FECHA_CREACION		DATE 			NOT NULL,
	LONGITUD	 		VARCHAR(100)	,
	LATITUD				VARCHAR(100)	,
	PESO				DOUBLE			,
	PRESION_MIN			VARCHAR(100)	,
	PRESION_MAX			VARCHAR(100)	,
	PASOS				INT				,
	
	PRIMARY KEY(ID),
	FOREIGN KEY (DNI) REFERENCES USUARIOS (DNI)

);

CREATE TABLE SECUENCIAS(

	NOMBRE_SECUENCIA	VARCHAR(100)	NOT NULL,
	VALOR_SECUENCIA		BIGINT			NOT NULL

);
